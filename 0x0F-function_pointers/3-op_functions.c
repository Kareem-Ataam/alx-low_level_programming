#include"3-calc.h"
/**
 * op_add - Calculate the sum of two numbers
 * @a: The first number
 * @b: The second number
 *
 * Return: The result of addition
 */
int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub - Calculate the difference between tow numbers
 * @a: The first number
 * @b: The second number
 *
 * Return: The result of subtraction
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - Calculate the multiplication of two numbers
 * @a: The first number
 * @b: The second number
 *
 * Return: The result of Multiplication
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - Calculate the division of two numbers
 * @a: The first number
 * @b: The second number
 *
 * Return: The result of division
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - Calculate the module operation of two number
 * @a: The first number
 * @b: The second number
 *
 * Return: The result of module operation
 */
int op_mod(int a, int b)
{
	return (a % b);
}
