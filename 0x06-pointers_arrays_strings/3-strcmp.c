#include<string.h>
/**
 * _strcmp - Compare two strings
 * @s1: The first string
 * @s2: The second string
 *
 * Return: (0) if they are equal
 * negative number if the first is less than the second
 * Positive number if the second is less than the first
 */
int _strcmp(char *s1, char *s2)
{
	return (strcmp(s1, s2));
}
